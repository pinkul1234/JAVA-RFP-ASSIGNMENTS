class Default{
	static int a;
	static float b;
	static double c;
	static long d;
	static char e;
	static boolean f;
	public static void main(String arg[])
	{
		System.out.println("int = "+a);
		System.out.println("float = "+b);
		System.out.println("double = "+c);
		System.out.println("long = "+d);
		System.out.println("char = "+e);
		System.out.println("boolean = "+f);
	}
}